package com.example.basics.data.types;

public class J04_boolean_char {
    public static void main(String args[]){

        //Typ boolean pozwala na zapisanie wartości logicznej: true lub false
        boolean flag1 = true;
        boolean flag2 = false;
        System.out.println("flag1: " + flag1);

        //Typ char pozwala na zapisanie pojedyńczego zanku Unicode dlatego ma długość 2 bajtów
        char sing1 = '\u004A' ;
        System.out.println("char sing: " + sing1);
    }
}
